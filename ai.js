function processDiagram() {
    const uploadInput = document.getElementById('uploadInput');
    const resultContainer = document.getElementById('resultContainer');

    if (uploadInput.files.length > 0) {
        const uploadedFile = uploadInput.files[0];
        const fileType = uploadedFile.type;

        if (fileType.startsWith('image/')) {
            const reader = new FileReader();

            reader.onload = function (e) {
                const imageUrl = e.target.result;

                // Simulate AI processing (replace this with actual AI logic)
                const aiDescription = simulateAIDescription();

                
                resultContainer.innerHTML = `
                    <img src="${imageUrl}" alt="Uploaded Diagram" style="max-width: 100%">
                    <p><strong>AI Description:</strong> ${aiDescription}</p>
                `;
            };

            reader.readAsDataURL(uploadedFile);
        } else {
            alert('Please upload a valid image file (e.g., JPG, JPEG, PNG).');
        }
    } else {
        alert('Please select a file to upload.');
    }
}

